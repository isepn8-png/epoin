<script>
  (function($){
    var $m = $('#epoinAboutModal');
    if(!$m.length || !$.fn || !$.fn.modal) return;

    // Pindahkan modal ke <body> agar z-index/backdrop aman
    $m.appendTo('body');

    // Auto-animasi list saat modal tampil + fit badge
    function ensureBadgeSingleLine(){
      $m.find('.eps-mod .title').each(function(){
        var $t = $(this), $badge = $t.find('.title-badge');
        if(!$badge.length) return;
        $badge.removeClass('badge-condense-1 badge-condense-2 badge-condense-3');
        $t.css('white-space','nowrap');
        var levels = ['badge-condense-1','badge-condense-2','badge-condense-3'];
        for(var i=0;i<levels.length && this.scrollWidth>this.clientWidth;i++){
          $badge.addClass(levels[i]);
        }
      });
    }

    $m.on('shown.bs.modal', function(){
      $m.find('.about-list li').each(function(i){
        this.style.animationDelay = (i*120)+'ms';
      });
      ensureBadgeSingleLine();
    });

    // Tap feedback utk benefit (mobile)
    $m.find('.eps-plus-head .eps-benefit').on('touchstart', function(){
      var el = this; el.classList.add('is-tap');
      setTimeout(function(){ el.classList.remove('is-tap'); }, 650);
    });

    $(window).on('resize orientationchange', ensureBadgeSingleLine);
    $(function(){ ensureBadgeSingleLine(); });

  })(window.jQuery || {});
</script>

<script>
(function($){
  var $m = $('#epoinAboutModal'); if(!$m.length) return;

  // Glow mengikuti kursor (halus, tidak memutihkan)
  $m.on('mousemove', '.eps-plus--v2 .eps-feature', function(e){
    var r = this.getBoundingClientRect();
    this.style.setProperty('--mx', (e.clientX - r.left) + 'px');
    this.style.setProperty('--my', (e.clientY - r.top) + 'px');
  }).on('mouseleave', '.eps-plus--v2 .eps-feature', function(){
    this.style.removeProperty('--mx'); this.style.removeProperty('--my');
  });

  // Enter/Space = animasi tap (aksesibilitas)
  $m.on('keydown', '.eps-plus--v2 .eps-feature', function(e){
    if(e.key === 'Enter' || e.key === ' '){ e.preventDefault(); this.classList.add('is-tap'); setTimeout(()=> this.classList.remove('is-tap'), 450); }
  });
})(window.jQuery);
</script>





